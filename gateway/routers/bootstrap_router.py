# ─────────────────────────────────────────
@router.get("/status")
async def bootstrap_status():
    """Trạng thái bootstrap hiện tại."""
    config = read_config()
    return {
        "bootstrapped": bool(ENV_FILE.exists()),
        "providers": {
            "openai":  bool(config.get("openai", {}).get("api_key")),
            "gemini":  bool(config.get("gemini", {}).get("api_key")),
            "copilot": bool(config.get("copilot", {}).get("token")),
        }
    }


# ── OpenAI ──────────────────────────────
@router.post("/openai/verify")
async def verify_openai(body: OpenAISetup):
    """Test API key bằng cách gọi models list."""
    async with httpx.AsyncClient() as client:
        resp = await client.get(
            "https://api.openai.com/v1/models",
            headers={"Authorization": f"Bearer {body.api_key}"},
            timeout=10,
        )
    if resp.status_code == 200:
        return {"valid": True, "message": "OpenAI key verified ✓"}
    raise HTTPException(status_code=400, detail=f"Invalid OpenAI key: {resp.status_code}")


