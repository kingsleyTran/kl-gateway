# ── GitHub Copilot Device Flow ────────────
@router.post("/copilot/start")
async def copilot_device_start():
    """
    Bắt đầu GitHub device flow.
    Trả về user_code và verification_uri để hiện lên UI.
    """
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            GITHUB_DEVICE_URL,
            data={
                "client_id": GITHUB_CLIENT_ID,
                "scope": GITHUB_SCOPES,
            },
            headers={"Accept": "application/json"},
            timeout=10,
        )

    if resp.status_code != 200:
        raise HTTPException(status_code=502, detail="Failed to start GitHub device flow")

    data = resp.json()
    return {
        "device_code":      data["device_code"],
        "user_code":        data["user_code"],
        "verification_uri": data["verification_uri"],
        "expires_in":       data["expires_in"],
        "interval":         data.get("interval", 5),
    }


@router.post("/copilot/poll")
async def copilot_device_poll(body: dict):
    """
    Poll GitHub token endpoint sau khi user nhập code.
    Frontend gọi endpoint này mỗi {interval} giây.
    """
    device_code = body.get("device_code")
    if not device_code:
        raise HTTPException(status_code=400, detail="device_code required")

    async with httpx.AsyncClient() as client:
        resp = await client.post(
            GITHUB_TOKEN_URL,
            data={
                "client_id":   GITHUB_CLIENT_ID,
                "device_code": device_code,
                "grant_type":  "urn:ietf:params:oauth:grant-type:device_code",
            },
            headers={"Accept": "application/json"},
            timeout=10,
        )

    data = resp.json()

    if "access_token" in data:
        return {"status": "success", "token": data["access_token"]}

    error = data.get("error", "unknown")
    if error == "authorization_pending":
        return {"status": "pending"}
    if error == "slow_down":
        return {"status": "slow_down"}
    if error in ("expired_token", "access_denied"):
        raise HTTPException(status_code=400, detail=f"Device flow failed: {error}")

    return {"status": "pending"}


# ── Save & Generate .env ─────────────────
@router.post("/save")
async def save_bootstrap(body: SaveConfig):
    """
    Lưu config và generate .env.
    Gateway sẽ cần restart để load .env mới.
    """
    config = {**body.providers, "default_model": body.default_model}
    save_config(config)
    write_env(config)

    return {
        "status": "saved",
        "message": "Configuration saved. Gateway will restart now.",
        "env_file": str(ENV_FILE),
    }
