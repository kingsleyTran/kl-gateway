"""
Bootstrap router — xử lý provider setup, auth, và .env generation.
"""

import os
import json
import asyncio
from pathlib import Path

import httpx
from fastapi import APIRouter, HTTPException
from fastapi.responses import JSONResponse
from pydantic import BaseModel

router = APIRouter()

ENV_FILE     = Path("/config/.env")
CONFIG_FILE  = Path("/config/providers.json")

# GitHub Device Flow constants
GITHUB_CLIENT_ID  = "Iv1.b507a08c87ecfe98"  # GitHub Copilot CLI client ID
GITHUB_DEVICE_URL = "https://github.com/login/device/code"
GITHUB_TOKEN_URL  = "https://github.com/login/oauth/access_token"
GITHUB_SCOPES     = "read:user"


# ─────────────────────────────────────────
# Schemas
# ─────────────────────────────────────────
class OpenAISetup(BaseModel):
    api_key: str

class GeminiSetup(BaseModel):
    api_key: str

class SaveConfig(BaseModel):
    providers: dict  # { "openai": {...}, "gemini": {...}, "copilot": {...} }
    default_model: str = "gpt-4.1"


# ─────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────
def write_env(config: dict):
    """Ghi .env từ config dict."""
    lines = ["# Generated by OpenClaw Gateway bootstrap\n"]

    if config.get("openai", {}).get("api_key"):
        lines.append(f'OPENAI_API_KEY={config["openai"]["api_key"]}\n')

    if config.get("gemini", {}).get("api_key"):
        lines.append(f'GEMINI_API_KEY={config["gemini"]["api_key"]}\n')

    if config.get("copilot", {}).get("token"):
        lines.append(f'COPILOT_TOKEN={config["copilot"]["token"]}\n')

    lines.append(f'DEFAULT_MODEL={config.get("default_model", "gpt-4.1")}\n')
    lines.append('PROJECTS_ROOT=/projects\n')
    lines.append('CHROMA_HOST=chromadb\n')
    lines.append('CHROMA_PORT=8000\n')
    lines.append('OLLAMA_HOST=ollama\n')
    lines.append('OLLAMA_PORT=11434\n')
    lines.append('EMBED_MODEL=nomic-embed-text\n')

    ENV_FILE.parent.mkdir(parents=True, exist_ok=True)
    ENV_FILE.write_text("".join(lines))


def read_config() -> dict:
    if CONFIG_FILE.exists():
        return json.loads(CONFIG_FILE.read_text())
    return {}


def save_config(config: dict):
    CONFIG_FILE.parent.mkdir(parents=True, exist_ok=True)
    CONFIG_FILE.write_text(json.dumps(config, indent=2))


# ─────────────────────────────────────────
